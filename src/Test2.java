public class Test2 {
    public static void main(String args[])
    {
        int lay=5;
        for(int i=1;i<=lay;i++)
        {
            for(int k=lay;k>i;k--)
            {
                System.out.print(" ");
            }
            for(int j=1;j<=(2*i-1);j++ )
            {
                if(i==1||i==lay) {
                    System.out.print("*");
                }else{
                    if(j==1||j==(2*i-1)){
                        System.out.print("*");
                    }else{
                        System.out.print(" ");
                    }
                }
            }
            System.out.println();
        }
    }
}
